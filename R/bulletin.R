  writeLines(rmd_content, template_path)

  message("Rendering bulletin to: ", output_file)
  rmarkdown::render(
    input       = template_path,
    output_file = output_file,
    quiet       = TRUE,
    envir       = new.env(parent = globalenv())
  )

  message("Bulletin generated: ", output_file)
  invisible(output_file)
}

# Internal helpers for recommendations
.get_irrigation_advice <- function(spi_val) {
  if (is.na(spi_val))        return("Data unavailable.")
  if (spi_val <= -2.0)       return("Emergency irrigation required. Activate all water sources.")
  if (spi_val <= -1.5)       return("Increase irrigation frequency. Monitor soil moisture daily.")
  if (spi_val <= -1.0)       return("Supplement natural rainfall with irrigation.")
  return("Normal irrigation schedule recommended.")
}

.get_crop_advice <- function(spi_val) {
  if (is.na(spi_val))        return("Data unavailable.")
  if (spi_val <= -2.0)       return("Consider drought-tolerant varieties. Delay planting if possible.")
  if (spi_val <= -1.5)       return("Prioritise high-value crops. Mulch to reduce evaporation.")
  if (spi_val <= -1.0)       return("Monitor crop stress. Apply foliar nutrition if needed.")
  return("Normal agronomic practices apply.")
}
